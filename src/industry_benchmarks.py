def get_benchmark(industry: str, metric_name: str):
    """获取指定行业的指定指标基准值"""
    ind = INDUSTRY_BENCHMARKS.get(industry, INDUSTRY_BENCHMARKS["其他"])
    return ind.get(metric_name, None)


def get_industry_benchmarks(industry: str) -> dict:
    """获取指定行业的全部基准值"""
    return INDUSTRY_BENCHMARKS.get(industry, INDUSTRY_BENCHMARKS["其他"])


def evaluate_metric(value, benchmark, metric_name: str, industry: str = "其他") -> str:
    """
    评价指标相对于基准的好坏
    返回: 'good' / 'warn' / 'danger' / 'unknown'
    """
    from math import isnan
    if value is None or (isinstance(value, float) and isnan(value)):
        return "unknown"
    if benchmark is None:
        return "unknown"

    # 越高越好的指标
    higher_better = {
        "流动比率", "速动比率", "利息保障倍数", "毛利率", "净利率", "ROE", "ROA",
        "EBITDA率", "总资产周转率", "经营现金流/净利润", "现金比率",
        "营业利润率", "成本费用利润率", "研发费用率", "Altman Z-score",
    }
    # 越低越好的指标
    lower_better = {
        "资产负债率", "存货周转天数", "应收账款周转天数", "实际税率",
        "综合税负率", "产权比率", "有息负债率", "销售费用率",
        "管理费用率", "财务费用率", "期间费用率", "营业成本率",
    }

    if metric_name in higher_better:
        if value >= benchmark * 0.9:
            return "good"
        elif value >= benchmark * 0.7:
            return "warn"
        else:
            return "danger"
    elif metric_name in lower_better:
        if value <= benchmark * 1.1:
            return "good"
        elif value <= benchmark * 1.5:
            return "warn"
        else:
            return "danger"
    else:
        return "unknown"
